#include "ft_ssl.h"

void	*inttochar_64(uint64_t *i)
{
	int			j;
	uint64_t	tmp;

	j = -1;
	tmp = 0;
	while (++j < 8)
	{
		tmp = tmp << 8;
		tmp |= (0xff & *i);
		*i = *i >> 8;
	}
	*i = tmp;
	return ((char *)i);
}
