#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*dst;

	if (!(dst = malloc(size)))
		return (NULL);
	if (dst != NULL)
		ft_memset(dst, '\0', size);
	else
		return (NULL);
	return (dst);
}
