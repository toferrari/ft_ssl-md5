#include "libft.h"

int		ft_nbword(char *s, char c)
{
	int		nbmot;
	int		i;

	nbmot = 0;
	i = 0;
	while (s[i] == c)
		i++;
	while (s[i])
	{
		if ((s[i] == c && s[i - 1] != c) || (s[i] != c && s[i + 1] == '\0'))
			nbmot++;
		i++;
	}
	return (nbmot);
}
